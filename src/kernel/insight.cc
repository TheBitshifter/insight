#include "kernel/insight.hh"

#include "utils/logs.hh"
#include "kernel/Expressions.hh"
#include "kernel/Architecture.hh"


static int init_count = 0;

void
insight::init(const ConfigTable &cfg)
{
  if (init_count == 0)
    {
      logs::init (cfg);
      Architecture::init ();
      Expr::init (cfg);
    }
  init_count++;
}

void
insight::terminate()
{
  init_count--;
  if (init_count == 0)
    {
      Expr::terminate ();
      Architecture::terminate ();
      logs::terminate ();
    }
}
